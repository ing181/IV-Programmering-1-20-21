#include <iostream>
using namespace std;
int main()
{

    for (int i=0; i<4; i++) {

        cout << i;  // 0 1 2 3
 // "int i=00", första gången, endast en gång
 // Växlar mellan vilkoret "i<4"
 // Om sant, gör "{  }"
 // Utför förändring "i++"
    }
// Hoppar hit när vilkoret "i<4" är falskt
    return 0;
}
